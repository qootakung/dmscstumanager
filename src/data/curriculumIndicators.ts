// Helper: Get all indicators for a specific grade
export const getIndicatorsForGrade = (grade: string): { group: SubjectGroup; subject: SubjectArea; standard: Standard; indicators: CurriculumIndicator[] }[] => {
  const result: { group: SubjectGroup; subject: SubjectArea; standard: Standard; indicators: CurriculumIndicator[] }[] = [];
  
  allSubjectGroups.forEach(group => {
    group.subjects.forEach(subject => {
      subject.standards.forEach(standard => {
        const indicators = standard.indicators[grade];
        if (indicators && indicators.length > 0) {
          result.push({ group, subject, standard, indicators });
        }
      });
    });
  });
  
  return result;
};

// Helper: Count total indicators for a grade
export const countIndicatorsForGrade = (grade: string): number => {
  let count = 0;
  allSubjectGroups.forEach(group => {
    group.subjects.forEach(subject => {
      subject.standards.forEach(standard => {
        const indicators = standard.indicators[grade];
        if (indicators) {
          count += indicators.length;
        }
      });
    });
  });
  return count;
};
